for (int i = 1; i < 11; i++) {
	url = "https://jsonplaceholder.typicode.com/photos/" + i
	RequestObject ro = new RestRequestObjectBuilder()
	.withRestUrl(url)
	.withRestRequestMethod("GET")
	.build()
	 
	ResponseObject respObj = WS.sendRequest(ro)
	
	if (respObj.getStatusCode() != 200) {
		KeywordUtil.markFailed("Status code is not 200 as expected. It is "
			+ respObj.getStatusCode())
	}
	
	WS.verifyElementPropertyValue(respObj, 'id', i)

}